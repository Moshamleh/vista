const output = `/* This file is generated by scripts/generate-openapi-client.mjs from the documented engine REST contract. */
export interface ApiEnvelope<T = unknown> {
  readonly data?: T
  readonly error?: { readonly code: string; readonly message: string }
  readonly meta?: { readonly requestId: string }
}

export interface GeneratedApiConfig {
  readonly baseUrl: string
  readonly apiKey: string
  readonly fetcher?: typeof fetch
}

export interface GeneratedRequestOptions {
  readonly path?: Record<string, string>
  readonly query?: Record<string, string | number | boolean | undefined>
  readonly body?: unknown
}

function buildPath(path: string, options: GeneratedRequestOptions): string {
  let next = path
  for (const [key, value] of Object.entries(options.path ?? {})) next = next.replace(\`{\${key}}\`, encodeURIComponent(value))
  const search = new URLSearchParams()
  for (const [key, value] of Object.entries(options.query ?? {})) {
    if (value !== undefined) search.set(key, String(value))
  }
  return search.size > 0 ? \`\${next}?\${search.toString()}\` : next
}

export async function requestGeneratedEndpoint(config: GeneratedApiConfig, method: string, path: string, options: GeneratedRequestOptions): Promise<ApiEnvelope> {
  const fetcher = config.fetcher ?? fetch
  const response = await fetcher(new URL(buildPath(path, options), config.baseUrl), {
    method,
    headers: {
      "content-type": "application/json",
      "x-api-key": config.apiKey
    },
    body: method === "GET" ? undefined : JSON.stringify(options.body ?? {})
  })
  const payload = (await response.json()) as ApiEnvelope
  if (!response.ok) throw new Error(payload.error?.message ?? \`Request failed with status \${String(response.status)}\`)
  return payload
}

export class GeneratedEngineClient {
  constructor(private readonly config: GeneratedApiConfig) {}

${methods}
}
`

